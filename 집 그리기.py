import turtle
win = turtle.Screen()
pen = turtle.Turtle()
win.setup(1200, 700)
win.title("SWEET HOME")
def readyDraw(target, x, y):
    target.penup()
    target.goto(x, y)
    target.pendown()
def drawBody(target, x, y, body_color, body_width, body_height):
    readyDraw(target, x, y)
    target.fillcolor(body_color)
    target.begin_fill()
    target.goto(x+body_width/2, y)
    target.goto(x+body_width/2, y-body_height)
    target.goto(x-body_width/2, y-body_height)
    target.goto(x-body_width/2, y)
    target.goto(x, y)
    target.end_fill()
def drawRoof(target, x, y, roof_color, roof_width, roof_height):
    readyDraw(target, x, y)
    target.fillcolor(roof_color)
    target.begin_fill()
    target.goto(x+roof_width/2, y)
    target.goto(x, y+roof_height)
    target.goto(x-roof_width/2, y)
    target.goto(x, y)
    target.end_fill()
def drawHouse(target, x, y, body_color, body_width, body_height, roof_color, roof_width, roof_height):
    drawBody(target, x, y, body_color, body_width, body_height)
    drawRoof(target, x, y, roof_color, roof_width, roof_height)
drawHouse(pen, 0, 0, 'black', 200, 200, 'red', 300, 150)